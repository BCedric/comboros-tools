    public function findOneByName(string $name)
    {
        return current($this->createQueryBuilder('b')
            ->where('b.name LIKE :val')
            ->orWhere(":val1 LIKE '%b.name%'")
            ->setParameter('val', "%" . $name . "%")
            ->setParameter('val1', $name)
            ->getQuery()
            ->getResult());
